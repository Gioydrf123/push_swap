#include <stddef.h>
#include "libft.h"

void	*ft_memcpy(void *s1, const void *s2, size_t n)
{
	const char	*s;
	char		*d;

	if (!s1 && !s2)
		return (NULL);
	s = s2;
	d = s1;
	while (n-- > 0)
		*d++ = *s++;
	return (s1);
}
